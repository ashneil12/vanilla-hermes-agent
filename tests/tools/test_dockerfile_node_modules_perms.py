"""contract test: Dockerfile ships runtime dependency trees owned by hermes

regression guard for #18800. the container drops privileges to the hermes
user (uid 10000) in entrypoint.sh, then the TUI launcher's
_tui_need_npm_install() trips on every startup (see the
npm_config_install_links=false comment in the Dockerfile) and runs
`npm install` in /opt/hermes/ui-tui. that install fails with EACCES unless
the runtime node_modules trees are owned by hermes.

The current image keeps that contract by copying the completed runtime tree
from a build stage with COPY --chown instead of adding a late recursive chown
layer.  The late chown fixed permissions, but it also duplicated metadata over
a large tree and made the published image much larger.

The production official-dashboard container runs as UID 1024, not the image's
default hermes UID 10000, so the Docker image must also force the TUI into
prebuilt bundle mode.  Otherwise the launcher tries to rebuild ui-tui/dist at
runtime and fails with EACCES.
"""
from __future__ import annotations

from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[2]
DOCKERFILE = REPO_ROOT / "Dockerfile"


def test_dockerfile_copies_runtime_tree_as_hermes_user() -> None:
    text = DOCKERFILE.read_text()

    assert (
        "COPY --from=build --chown=hermes:hermes /opt/hermes /opt/hermes" in text
    ), (
        "Dockerfile must copy the finished runtime tree as hermes so .venv "
        "and runtime node_modules stay writable after privilege drop; see #18800"
    )

    assert "chown -R hermes:hermes /opt/hermes/.venv" not in text, (
        "Do not restore the late recursive chown layer: it fixes ownership by "
        "duplicating a large runtime tree and regresses image size."
    )

    assert "root node_modules writable by the hermes user" in text


def test_dockerfile_uses_prebuilt_tui_bundle_at_runtime() -> None:
    text = DOCKERFILE.read_text()

    assert "ENV HERMES_TUI_DIR=/opt/hermes/ui-tui" in text, (
        "Container runtime must use the prebuilt self-contained TUI bundle. "
        "Production dashboard containers run as UID 1024 and cannot rebuild "
        "ui-tui/dist inside the immutable image."
    )
    assert "rm -rf /opt/hermes/web /opt/hermes/ui-tui/node_modules" in text, (
        "ui-tui/dist/entry.js is bundled by esbuild; runtime node_modules are "
        "not needed when HERMES_TUI_DIR points at the prebuilt bundle."
    )


def test_dockerfile_drops_build_only_caches_before_runtime_stage() -> None:
    text = DOCKERFILE.read_text()

    assert "FROM debian:13.4 AS runtime_base" in text
    assert "FROM runtime_base AS build" in text
    assert text.rstrip().count("FROM runtime_base") == 2
    assert "rm -rf /root/.cache /root/.npm /tmp/*" in text
    assert "uv cache clean" in text
    assert "rm -rf /opt/hermes/web" in text, (
        "The web workspace is build-time only because HERMES_WEB_DIST points "
        "at hermes_cli/web_dist; keeping web/node_modules bloats the runtime image."
    )


def test_dockerfile_keeps_playwright_runtime_deps_in_final_stage() -> None:
    text = DOCKERFILE.read_text()

    assert "npx playwright install chromium --only-shell" in text
    assert "--with-deps" not in text, (
        "Playwright OS packages must be installed in runtime_base; installing "
        "them via npm's build stage would omit them from the final image."
    )
    for package in ("libnss3", "libgbm1", "fonts-liberation"):
        assert package in text
